package solution;

public class Solution {
	private int solutionID;
	private String userID;
	private String solutionDate;
	private String solutionLanguage;
	private String solutionContent;
	private int solutionAvailable;
	public int getSolutionID() {
		return solutionID;
	}
	public void setSolutionID(int solutionID) {
		this.solutionID = solutionID;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getSolutionDate() {
		return solutionDate;
	}
	public void setSolutionDate(String solutionDate) {
		this.solutionDate = solutionDate;
	}
	public String getSolutionLanguage() {
		return solutionLanguage;
	}
	public void setSolutionLanguage(String solutionLanguage) {
		this.solutionLanguage = solutionLanguage;
	}
	public String getSolutionContent() {
		return solutionContent;
	}
	public void setSolutionContent(String solutionContent) {
		this.solutionContent = solutionContent;
	}
	public int getSolutionAvailable() {
		return solutionAvailable;
	}
	public void setSolutionAvailable(int solutionAvailable) {
		this.solutionAvailable = solutionAvailable;
	}
}
